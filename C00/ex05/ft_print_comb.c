#include <unistd.h>
void    ft_print_comb(void)
{
        int     a;
        int     b;
        int     c;

        a = '0';
        b = '1';
        c = '2';
        while (a < '8')
                {
                                while ( b < '9')
                                {
                                        while ( c < '10')
                                        {
                                                char    numero[4];
                                                numero[0] = a + '0';
                                                numero[1] = b + '0';
                                                numero[3] = c + '0';

                                                write(1, &numero, 3);

                                                if (a !=7 || b !=8 || c !=9)
                                                {
                                                        write(1, ", ", 2);
                                                }

                                        c++;
                                }

                                b++;
                                c = b +1;
                        }

                        a++;
                        b = a + 1;
                        c = b + 1;
                }
}
